package com.trm.model.jest;

public class ExpertInfoJest {
    private String id;
    private String expert_name;
    private String expert_domain;
    private String expert_story;
    private String expert_research;
    private String create_date;
    private String create_time;
    private String update_date;
    private String update_time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getExpert_name() {
        return expert_name;
    }

    public void setExpert_name(String expert_name) {
        this.expert_name = expert_name;
    }

    public String getExpert_domain() {
        return expert_domain;
    }

    public void setExpert_domain(String expert_domain) {
        this.expert_domain = expert_domain;
    }

    public String getExpert_story() {
        return expert_story;
    }

    public void setExpert_story(String expert_story) {
        this.expert_story = expert_story;
    }

    public String getExpert_research() {
        return expert_research;
    }

    public void setExpert_research(String expert_research) {
        this.expert_research = expert_research;
    }

    public String getCreate_date() {
        return create_date;
    }

    public void setCreate_date(String create_date) {
        this.create_date = create_date;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getUpdate_date() {
        return update_date;
    }

    public void setUpdate_date(String update_date) {
        this.update_date = update_date;
    }

    public String getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(String update_time) {
        this.update_time = update_time;
    }
}
